public class Livro {
    public String titulo;
    public String autor;
    public String editora;
    public String isbn;
    public int anopubli;

    public Livro(String titulo, String autor, String editora, String isbn, int anopubli){
        this.titulo = titulo;
        this.autor = autor;
        this.editora = editora;
        this.isbn = isbn;
        this.anopubli = anopubli;
    }

}
